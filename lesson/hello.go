func BinaryConversion() {
	a := 10
	fmt.Printf("dec: %d\n", a)
	fmt.Printf("hex downcase: %x\n", a)
	fmt.Printf("hex upcase: %X\n", a)
	fmt.Printf("octal: %o\n", a)
	fmt.Printf("bin: %b\n", a)

	s := "str"
	fmt.Printf("string: %s\n", s)

	f := 3.1
	fmt.Printf("%%f: %f\n", f)
	fmt.Printf("%%g: %g\n", f)
	fmt.Printf("%%e: %e\n", f)
	fmt.Printf("%%v: %v\n", f)

	fmt.Printf("%%T of 10: %T\n", a)
	fmt.Printf("%%T of str: %T\n", s)
	fmt.Printf("%%T of 3.1: %T\n", f)
}

func Dup2() {
	counts := make(map[string]int)
	files := os.Args[1:]
	if len(files) == 0 {
		countLines(os.Stdin, counts)
	} else {
		for _, arg := range files {
			f, err := os.Open(arg)
			if err != nil {
				fmt.Fprint(os.Stderr, "dup2: %v\n", err)
				continue
			}
			countLines(f, counts)
			f.Close()
		}
	}

}
func countLines(f *os.File, counts map[string]int) {
	input := bufio.NewScanner(f)
	for input.Scan() {
		counts[input.Text()]++
	}

	for k, v := range counts {
		if v > 1 {
			fmt.Printf("重复次数：%d\t输入内容：%s\n", v, k)
		}
	}
}

